    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
         String id, n, q, p;
        id = txtIdProd.getText();
        n = txtNomeProd.getText();
        q = txtQuantidade.getText();
        p = txtValor.getText();
        if (id.isEmpty()) { // se usuário está vazio
            JOptionPane.showMessageDialog(null, "É obrigatório digitar o Id do produto");
            txtIdProd.requestFocus();
            return; // stop
        }
        if (n.isEmpty()) { // se senha está vazia
            JOptionPane.showMessageDialog(null, "É obrigatório digitar o nome do produto");
            txtNomeProd.requestFocus();
            return; // stop
        }
        try {
            //Salvar os dados do usuário
            SistemaDao dao;
            dao = new SistemaDao();
            dao.salvarProduto(id,n,q,p);

            //4.1 Mostrar a mensagem "Usuário salvo com sucesso" e limpar os campos.
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso");
            txtIdProd.setText("");
            txtNomeProd.setText("");
            txtQuantidade.setText(""); 
            txtValor.setText("");
            txtIdProd.requestFocus();

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Mensagem de erro:" + ex.getMessage());
        } 
        catch (SQLException ex) {
            int erro = ex.getErrorCode();
            if (erro == 1062) {
                JOptionPane.showMessageDialog(null, "Usuário Já cadastrado");
                txtIdProd.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "ENtre em contato com o administrador do sistema e informe o erro: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta; // 0 - Sim; 1 - Não 
        resposta = JOptionPane.showConfirmDialog(null, "Confirma?", "Confirmação", 0);
        //1.1 - Se a responta for sim
        if (resposta == 0) { //Resposta SIM
            //1.1.1 Obter o usuário no txtUsuario.
            String u = txtIdProd.getText();

            try {
                //Excluir o usuário
                SistemaDao dao;
                dao = new SistemaDao();
                dao.excluirProduto(u);

                //1.1.4 Mostrar a mensagem "Usuário excluído com sucesso" e limpar os campos.
                JOptionPane.showMessageDialog(null, "Produto excluído com sucesso");
                //1.1.5 Fecha a TelaUsuario 
                dispose();

            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Mensagem de erro:" + ex.getMessage());
            }
            //1.2 - Se a resposta for não
            //1.2.2 Fecha a TelaUsuario  
        } else {
            dispose();
        }
       
    }//GEN-LAST:event_btnExcluirActionPerformed

