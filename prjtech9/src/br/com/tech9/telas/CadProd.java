     private void abrirTelaProduto(){
        // 1 - Solicitar o usuário
        String p = JOptionPane.showInputDialog("Qual o ID do produto que voce deseja?");
        if(p == null){
            return; //stop
        }
        try {
             SistemaDao dao;
             dao = new SistemaDao();
             ResultSet resultado;
             resultado = dao.buscarProduto(p);

            //4 - Verificar se o usuário foi encontrado na tabela usuario do banco de dados.
            if (resultado.next()) {
                //Pega o nome e o cargo que veio na consulta ao banco de dados
                String idProd, nome, preco, quantidade;
                idProd = resultado.getString("idproduto");
                nome = resultado.getString("nome");
                preco = resultado.getString("preco");
                quantidade = resultado.getString("quantidade");
                //Abrir o formulário Menu.java
                this.dispose();
                Alterar alterar;
                alterar = new Alterar(idProd, nome, preco, quantidade);
                alterar.setVisible(true);
                
            } else {
                JOptionPane.showMessageDialog(null, "Produto não encontrado");
            }

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Driver não está na library");
        } 
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Você errou nos dados da conexão com o banco de dados");
        }
     }
    

